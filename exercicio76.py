#Faça um Programa que calcule a área de uma sala de um apartamento. Para isso, o seu programa precisa pedir a largura da sala, o comprimento da sala e imprimir a área em m² da sala.

base = float(input('Digite a base para calcular a área da sua sala: '))
altura = float(input('Digite a altura da sala: '))
area = base * altura
print(f'A área da sala de um apartamento é: {area} m² ')

